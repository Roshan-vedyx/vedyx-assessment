// src/lib/pdf-generator.ts
import { jsPDF } from 'jspdf'

export interface ReportData {
  childName: string
  childAge: string
  primaryLearningStyle: string
  scores: any
  recommendations: string[]
  strategies: string[]
}

export function generatePDFReport(data: ReportData): Uint8Array {
  const doc = new jsPDF({
    orientation: 'portrait',
    unit: 'mm',
    format: 'a4'
  })

  // Page setup
  const pageWidth = doc.internal.pageSize.getWidth()
  const pageHeight = doc.internal.pageSize.getHeight()
  const margin = 20
  const lineHeight = 7
  let yPos = margin

  // Helper functions
  const addText = (text: string, fontSize = 12, style: 'normal' | 'bold' = 'normal') => {
    doc.setFont('helvetica', style)
    doc.setFontSize(fontSize)
    const lines = doc.splitTextToSize(text, pageWidth - 2 * margin)
    doc.text(lines, margin, yPos)
    yPos += lines.length * lineHeight
    return lines.length * lineHeight
  }

  const addSection = (title: string, content: string) => {
    // Check if we need a new page
    if (yPos > pageHeight - 40) {
      doc.addPage()
      yPos = margin
    }
    
    addText(title, 16, 'bold')
    yPos += 5
    addText(content, 11)
    yPos += 10
  }

  const addHeader = () => {
    // Header background
    doc.setFillColor(79, 70, 229) // Blue
    doc.rect(0, 0, pageWidth, 40, 'F')
    
    // Title
    doc.setTextColor(255, 255, 255) // White
    doc.setFont('helvetica', 'bold')
    doc.setFontSize(20)
    doc.text(`${data.childName}'s Learning Assessment`, pageWidth/2, 25, { align: 'center' })
    
    // Reset color
    doc.setTextColor(0, 0, 0)
    yPos = 50
  }

  // Generate PDF content
  addHeader()

  // Executive Summary
  addSection(
    'Executive Summary',
    `This comprehensive assessment reveals that ${data.childName} (age ${data.childAge}) demonstrates a primary ${data.primaryLearningStyle} learning preference. The following report provides evidence-based strategies to support their academic success.`
  )

  // Learning Style Analysis
  addSection(
    'Primary Learning Style: ' + data.primaryLearningStyle,
    `Based on the assessment responses, ${data.childName} shows strongest alignment with ${data.primaryLearningStyle.toLowerCase()} learning approaches. This means they process and retain information most effectively through this modality.`
  )

  // Key Recommendations
  let recText = data.recommendations.map((rec, i) => `${i + 1}. ${rec}`).join('\n\n')
  addSection('Key Recommendations', recText)

  // Implementation Strategies
  let stratText = data.strategies.map((strat, i) => `${i + 1}. ${strat}`).join('\n\n')
  addSection('Implementation Strategies', stratText)

  // Footer
  doc.setFontSize(8)
  doc.setTextColor(128, 128, 128)
  doc.text('Generated by Vedyx Learning Assessment Tool', pageWidth/2, pageHeight - 10, { align: 'center' })
  
  return doc.output('arraybuffer')
}

